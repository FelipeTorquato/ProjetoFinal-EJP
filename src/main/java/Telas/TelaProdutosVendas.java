    private void jButtonAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonAddActionPerformed
        DefaultTableModel dtmProdutos = (DefaultTableModel) jTableProdutos.getModel();
        try {
            if (Integer.parseInt(jTextFieldQuantidade.getText()) < 0) {
                JOptionPane.showMessageDialog(null, """
                                                Processo n\u00e3o finalizado.
                                                Quantidade menor que 0.""");
            } else {
                Object[] dados = {jTextFieldCodigo.getText(), jTextFieldNome.getText(), jTextFieldQuantidade.getText(), jTextFieldCritico.getText(), Double.parseDouble(jTextFieldCritico.getText()) >= Double.parseDouble(jTextFieldQuantidade.getText()) ? "CRÍTICO" : "NÃO CRÍTICO"};
                dtmProdutos.addRow(dados);
            }
        } catch (Exception e) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher campos.");
        }

    }//GEN-LAST:event_jButtonAddActionPerformed

    private void jButtonDeletarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonDeletarActionPerformed
        if (jTableProdutos.getSelectedRow() != -1) {
            DefaultTableModel dtmProdutos = (DefaultTableModel) jTableProdutos.getModel();
            dtmProdutos.removeRow(jTableProdutos.getSelectedRow());
        } else {
            JOptionPane.showMessageDialog(null, "Selecione um produto para excluir.");
        }

        if (jTableProdutos.getRowCount() == 0) {
            jLabelAvisoTexto.setVisible(false);
        } else {
            int countCrit = 0;

            for (int i = 0; i < jTableProdutos.getRowCount(); i++) {
                if (jTableProdutos.getValueAt(i, 4).toString().equals("CRÍTICO")) {
                    countCrit++;
                }
            }
            if (countCrit == 0) {
                jLabelAvisoTexto.setVisible(false);
            } else {
                jLabelAvisoTexto.setVisible(true);
            }
        }
    }//GEN-LAST:event_jButtonDeletarActionPerformed

