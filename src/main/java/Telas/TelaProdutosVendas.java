    private void jButtonVendaActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonVendaActionPerformed
        // TODO add your handling code here:
        if (jTextFieldCodigoVenda.getText().equals("") || jTextFieldCodigoProd.getText().equals("") || jTextFieldQuantidadeVendas.getText().equals("") || jTextFieldPreco.getText().equals("") || (Integer.parseInt(jTextFieldQuantidadeVendas.getText()) < 0) || (Integer.parseInt(jTextFieldPreco.getText()) < 0)) {
            JOptionPane.showMessageDialog(null, "Erro ao preencher campos.");
        } else {
            if (Double.parseDouble(jTextFieldPreco.getText()) < 0) {
                JOptionPane.showMessageDialog(null, """
                                                Compra n\u00e3o finalizada.
                                                Pre\u00e7o menor que 0.""");
            } else {
                DefaultTableModel dtmVendas = (DefaultTableModel) jTableVendas.getModel();
                int i = 0;
                Object nomeProduto = null;
                int valorCodigo = Integer.parseInt(jTextFieldCodigoProd.getText());
                for (i = 0; i < jTableProdutos.getRowCount(); i++) {
                    if (valorCodigo == Integer.parseInt(jTableProdutos.getValueAt(i, 0).toString())) {
                        if (Integer.parseInt(jTableProdutos.getValueAt(i, 2).toString()) - Integer.parseInt(jTextFieldQuantidadeVendas.getText()) < 0) {
                            JOptionPane.showMessageDialog(null, """
                                                            Compra n\u00e3o finalizada
                                                            Quantidade insuficiente no estoque.""");
                        } else {
                            nomeProduto = jTableProdutos.getValueAt(i, 1);
                            int quantVendida = Integer.parseInt(jTextFieldQuantidadeVendas.getText());
                            int quantDisponivel = Integer.parseInt(jTableProdutos.getValueAt(i, 2).toString());
                            Object novaQuant = quantDisponivel - quantVendida;
                            jTableProdutos.setValueAt(novaQuant, i, 2);

                            Object[] dados = {jTextFieldCodigoVenda.getText(), nomeProduto.toString(), jTextFieldQuantidadeVendas.getText(), jFormattedTextField.getText()};
                            dtmVendas.addRow(dados);

                            if (Double.parseDouble(jTableProdutos.getValueAt(i, 3).toString()) >= Double.parseDouble(jTableProdutos.getValueAt(i, 2).toString())) {
                                jTableProdutos.setValueAt("CRÍTICO", i, 4);
                                jLabelAvisoTexto.setVisible(true);
                            }
                        }
                    }
                }
            }
        }


    }//GEN-LAST:event_jButtonVendaActionPerformed

