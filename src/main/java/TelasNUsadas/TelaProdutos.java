    private void jButtonEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButtonEditarActionPerformed
        // TODO add your handling code here:
        if (jTableProdutos.getSelectedRow() != -1) {
            jTableProdutos.setValueAt(jTextFieldCodigo.getText(), jTableProdutos.getSelectedRow(), 0);
            jTableProdutos.setValueAt(jTextFieldNome.getText(), jTableProdutos.getSelectedRow(), 1);
            jTableProdutos.setValueAt(jTextFieldQuantidade.getText(), jTableProdutos.getSelectedRow(), 2);
            jTableProdutos.setValueAt((int) Math.floor(Double.parseDouble(jTextFieldQuantidade.getText()) * 0.1), jTableProdutos.getSelectedRow(), 3);
        }
    }//GEN-LAST:event_jButtonEditarActionPerformed

