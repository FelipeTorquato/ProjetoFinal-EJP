class Produtos {
        private String codigo;
        private String nome;
        private int quant;
        private int quantCrit;

        Produtos(String codigo, String nome, int quant, int quantCrit) {
            this.codigo = codigo;
            this.nome = nome;
            this.quant = quant;
            this.quantCrit = quantCrit;
        }
        
        public String getCodigo() {
            return codigo;
        }

        public void setCodigo(String codigo) {
            this.codigo = codigo;
        }

        public String getNome() {
            return nome;
        }

        public void setNome(String nome) {
            this.nome = nome;
        }

        public int getQuant() {
            return quant;
        }

        public void setQuant(int quant) {
            this.quant = quant;
        }

        public int getQuantCrit() {
            return quantCrit;
        }

        public void setQuantCrit(int quantCrit) {
            this.quantCrit = quantCrit;
        }
        
    }

